package top.xiwangly.test;

public class 算法表达式测试 {
    public static void main(String[] args) {
        int n = 4;
        while (n!=0){
            System.out.println(n);
            n--;
        }
        System.out.println("==================");
        int n2 = 4;
        while (n2 -- > 0){
            System.out.println(n2);
        }
    }
}
